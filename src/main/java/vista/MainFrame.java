    public void registrar(String usuario, String password){
        String pass = aes.encrypt(password);
        Usuarios usuarios = new Usuarios(usuario, pass);
        controller.guardaUsuario(usuarios);
        this.remove(registroPanel);
        this.add(loginPanel);
        this.repaint();
        this.revalidate();
    }

